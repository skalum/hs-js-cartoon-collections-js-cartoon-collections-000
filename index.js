function dwarfRollCall(dwarves) {
  const numDwarves = dwarves.length;
  var numberedDwarves = [];

  for (let i = 0; i < numDwarves; i++) {
    numberedDwarves.push(`${i + 1}. ${dwarves[i]}`)
  }

  return numberedDwarves.join(" ");
}

